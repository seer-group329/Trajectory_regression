function [x,y] = polynomialConnection(X, Y)
    t0 = 0;
    t1 = 1;
    T = [ t0^5      t0^4      t0^3     t0^2    t0   1;
        5*t0^4    4*t0^3    3*t0^2   2*t0    1    0;
        20*t0^3   12*t0^2   6*t0     1       0    0;
        t1^5      t1^4      t1^3     t1^2    t1   1;

        5*t1^4    4*t1^3    3*t1^2   2*t1    1    0;
        20*t1^3   12*t1^2   6*t1     1       0    0];

    A = T \ X';
    B = T \ Y';
    t = t0:0.01:t1;
    x = (A(1)*t.^5  + A(2)*t.^4 + A(3)*t.^3 + A(4)*t.^2 + A(5)*t + A(6));
    y = (B(1)*t.^5  + B(2)*t.^4 + B(3)*t.^3 + B(4)*t.^2 + B(5)*t + B(6));
end
